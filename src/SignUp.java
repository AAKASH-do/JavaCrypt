    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String firstName = jTextField1.getText();
        String lastname = jTextField3.getText();
        String email = jTextField2.getText();
        String password = new String(jPasswordField1.getPassword());
        
        if (firstName.isEmpty() || lastname.isEmpty() || email.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill all fields");
            return;
        }
        
        try{
            SecretKey secretKey = SecureEncryption.generateSecretKey();
            byte[] iv = SecureEncryption.generateIV();
            String encryptedPassword = SecureEncryption.encrypt(password, secretKey, iv);
            saveToDatabase(firstName, lastname, email, encryptedPassword, secretKey, iv);
        }catch (Exception ex){
            JOptionPane.showMessageDialog(this, "Error during sign-up. Please try again.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void saveToDatabase(String firstName, String lastName, String email, String encryptedPassword, SecretKey secretKey, byte[] iv) {
    // PostgreSQL connection parameters
        Dotenv dotenv = Dotenv.load();
        String url = dotenv.get("DB_URL");  // Replace with your database URL
        String user = dotenv.get("DB_USER");
        String password = dotenv.get("DB_PASSWORD"); 

        // Load PostgreSQL driver
        try {
            Class.forName("org.postgresql.Driver");
        } catch (ClassNotFoundException e) {
            JOptionPane.showMessageDialog(this, "PostgreSQL JDBC Driver not found. Include it in your library path.");
            return;  // Exit the method if the driver is not found
        }

        // SQL insert query
        String insertSQL = "INSERT INTO public.users(first_name, last_name, email, password, secret_key, iv) VALUES (?, ?, ?, ?, ?, ?);";

        try (Connection conn = DriverManager.getConnection(url, user, password);
             PreparedStatement pstmt = conn.prepareStatement(insertSQL)) {

            pstmt.setString(1, firstName);
            pstmt.setString(2, lastName);
            pstmt.setString(3, email);
            pstmt.setString(4, encryptedPassword);
            pstmt.setBytes(5, secretKey.getEncoded());  // Save secret key as binary
            pstmt.setBytes(6, iv);  // Save IV as binary

            pstmt.executeUpdate();  // Execute insert statement

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Failed to save data to the database: " + e.getMessage());
        }
    }


